import math

# Task 1: Ceil of 2.3
n1 = 2.3
ceil = math.ceil(n1)
print(f"For n = {n1}, ceil: {ceil}")

# Task 2: Floor of 2.3
n2 = 2.3
floor = math.floor(n2)
print(f"For n = {n2}, floor: {floor}")

# Task 3: Factorial of 5
n3 = 5
factorial = math.factorial(n3)
print(f"For n = {n3}, factorial: {factorial}")

# Task 4: Power of 2 to the power of 3
n4 = 2
power = math.pow(n4, 3)
print(f"For a = {n4}, 2^3: {power}")

# Task 5: Square root of 16
n5 = 16
square_root = math.sqrt(n5)
print(f"For b = {n5}, square root: {square_root}")
